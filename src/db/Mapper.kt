package elite.restapi.db

import elite.restapi.model.Todo
import elite.restapi.model.User
import elite.restapi.repository.Todos
import elite.restapi.repository.Users
import org.jetbrains.exposed.sql.ResultRow

object Mapper {

    fun rowToUser(row: ResultRow?): User? {
        if (row == null) {
            return null
        }
        return User(
            userId = row[Users.userId],
            email = row[Users.email],
            displayName = row[Users.displayName],
            passwordHash = row[Users.passwordHash]
        )
    }

    fun rowToTodo(row: ResultRow?): Todo? {
        if (row == null) {
            return null
        }
        return Todo(
            id = row[Todos.id],
            userId = row[Todos.userId],
            todo = row[Todos.todo],
            done = row[Todos.done]
        )
    }

}